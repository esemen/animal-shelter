                <p>
                  In 2004 Sylvia and Bill Van Atta bought a property called Cawdor Kennels in Carmarthenshire, Wales. The previous owners had a yard with kennels used for show dogs and they used these kennels to open their new rescue called Many Tears Animal Rescue.</p>

<p>Both Sylvia and Bill have a long history of animal rescue - particularly Sylvia who has set up and worked in rescues in both the UK and USA. They have witnessed many sad situations and shed many tears over dogs in terrible situations and hence the name Many Tears Animal Rescue.
</p>
<p>
Although Many Tears is based in Wales it has dogs in foster homes throughout the UK. We take in and rehome ex-breeding dogs, those from pounds, others whose owners are no longer able to keep them and, when we have space, we help rescues overseas.
</p>
<p>
Many Tears is not currently a registered charity but this is something that may be considered in the future. However, it is a home based rescue which means Sylvia and Bill run the rescue from their home and the rescue operates from their property. In addition to owning the land and running the rescue, Bill operates a boarding business from the same property which is how they support themselves. So they have themselves, a business and the rescue all sharing the house and property and all of this has to be taken into consideration.
                </p>

